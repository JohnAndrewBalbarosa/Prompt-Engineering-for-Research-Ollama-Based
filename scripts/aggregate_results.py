#!/usr/bin/env python3
"""Aggregate per-(strategy, model) metrics into a single summary table.

Reads every results/runs/by_strategy/<strategy>/<model>/metrics_summary.json,
builds a baseline-vs-strategy comparison (baseline = zero_shot), and writes:
  - results/summary.csv   (machine-readable)
  - results/summary.md    (README-ready table)

Run from the repo root:
    python scripts/aggregate_results.py
"""
from __future__ import annotations

import csv
import json
from pathlib import Path

BASELINE = "zero_shot"
ROOT = Path(__file__).resolve().parents[1]
RUNS = ROOT / "results" / "runs" / "by_strategy"
OUT_DIR = ROOT / "results"

# Friendly display names for model ids.
MODEL_LABELS = {
    "ollama_gemma2_9b": "gemma2:9b",
    "ollama_llama2_13b_chat": "llama2:13b-chat",
    "ollama_mistral_7b_instruct": "mistral:7b-instruct",
    "ollama_phi3_mini": "phi3:mini",
    "ollama_qwen2_5_7b": "qwen2.5:7b",
}


def load_metrics() -> dict[str, dict[str, dict]]:
    """Return {strategy: {model_id: metrics_dict}}."""
    table: dict[str, dict[str, dict]] = {}
    for strat_dir in sorted(p for p in RUNS.iterdir() if p.is_dir()):
        for model_dir in sorted(p for p in strat_dir.iterdir() if p.is_dir()):
            mfile = model_dir / "metrics_summary.json"
            if not mfile.exists():
                continue
            data = json.loads(mfile.read_text())
            row = data[0] if isinstance(data, list) else data
            table.setdefault(strat_dir.name, {})[model_dir.name] = row
    return table


def main() -> None:
    table = load_metrics()
    strategies = sorted(table.keys())
    comparison = [s for s in strategies if s != BASELINE]
    models = sorted({m for s in table.values() for m in s})

    OUT_DIR.mkdir(parents=True, exist_ok=True)

    # --- CSV (long format) ---
    csv_path = OUT_DIR / "summary.csv"
    with csv_path.open("w", newline="") as fh:
        w = csv.writer(fh)
        w.writerow(["model_id", "strategy", "accuracy", "correct", "total", "avg_latency_ms"])
        for strat in strategies:
            for model in models:
                r = table.get(strat, {}).get(model)
                if not r:
                    continue
                w.writerow([
                    model, strat, f"{r['accuracy']:.4f}",
                    r["correct_rows"], r["total_rows"], f"{r['avg_latency_ms']:.0f}",
                ])

    # --- Markdown comparison table (baseline vs each strategy) ---
    lines: list[str] = []
    base_rows = table.get(BASELINE, {})
    n_total = next(iter(base_rows.values()))["total_rows"] if base_rows else "?"
    header_cols = ["Model", f"{BASELINE} (baseline)"] + comparison + ["Δ best vs baseline"]
    lines.append("| " + " | ".join(header_cols) + " |")
    lines.append("| " + " | ".join(["---"] * len(header_cols)) + " |")

    base_vals, best_vals = [], []
    # Sort models by baseline accuracy descending for readability.
    def base_acc(m: str) -> float:
        r = table.get(BASELINE, {}).get(m)
        return r["accuracy"] if r else -1.0

    for model in sorted(models, key=base_acc, reverse=True):
        label = MODEL_LABELS.get(model, model)
        b = table.get(BASELINE, {}).get(model)
        b_acc = b["accuracy"] if b else None
        cells = [label, f"{b_acc:.0%}" if b_acc is not None else "—"]
        comp_accs = []
        for strat in comparison:
            r = table.get(strat, {}).get(model)
            if r:
                cells.append(f"{r['accuracy']:.0%}")
                comp_accs.append(r["accuracy"])
            else:
                cells.append("—")
        if b_acc is not None and comp_accs:
            best = max(comp_accs)
            delta = best - b_acc
            cells.append(f"{delta:+.0%}")
            base_vals.append(b_acc)
            best_vals.append(best)
        else:
            cells.append("—")
        lines.append("| " + " | ".join(cells) + " |")

    if base_vals:
        mb = sum(base_vals) / len(base_vals)
        mbest = sum(best_vals) / len(best_vals)
        comp_means = []
        for strat in comparison:
            accs = [r["accuracy"] for m in models if (r := table.get(strat, {}).get(m))]
            comp_means.append(f"**{sum(accs) / len(accs):.0%}**" if accs else "—")
        lines.append(
            "| **Mean** | **{:.0%}** | {} | **{:+.0%}** |".format(
                mb, " | ".join(comp_means), mbest - mb,
            )
        )

    md = (
        f"_Dataset: GSM8K · {n_total} questions per cell · exact-match final-answer accuracy · "
        f"baseline = `{BASELINE}` · generated by `scripts/aggregate_results.py`._\n\n"
        + "\n".join(lines)
        + "\n"
    )
    (OUT_DIR / "summary.md").write_text(md, encoding="utf-8")
    print(f"Wrote {csv_path}")
    print(f"Wrote {OUT_DIR / 'summary.md'}")
    print("\n" + md)


if __name__ == "__main__":
    main()
